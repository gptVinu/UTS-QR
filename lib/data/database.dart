  // Get popular stations
  static List<Map<String, dynamic>> getPopularStations() {
    return allStations
        .where((station) => station['isPopular'] == true)
        .map((station) => {
              'name': station['name'],
              'icon': Icons.train_outlined,
              'color': station['color'],
            })
        .toList();
  }

  // Get stations by line
  static List<Map<String, dynamic>> getStationsByLine(String line) {
    return allStations
        .where((station) => station['line'].toString().contains(line))
        .toList();
  }

  // Get all stations for search
